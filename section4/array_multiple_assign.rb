a, b = [1, 2]
p a # -> 1
p b # -> 2

# 代入先が多い場合はnilが入る
c, d = [10]
p c # -> 10
p d # -> nil

# 右辺の数が多い場合ははみ出した値が切り捨てられる
e, f = [100, 200, 300]
p e # -> 100
p f # -> 200

# 返り値が配列のメソッドを多重代入で受け取る
# 商とあまりを配列で返す
14.divmod(3) # -> [4,2]

# 戻り値を配列のまま受け取る
quo_rem = 14.divmod(3)
p "商=#{quo_rem[0]},余り=#{quo_rem[1]}" # -> "商=4,余り=2"

quotient, remainder = 14.divmod(3)
p "商=#{quotient},余り=#{remainder}" # -> "商=4,余り=2"
